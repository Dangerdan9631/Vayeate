/**
 * Writes code-review/checklist.md with one checkbox per in-scope source file
 * (src/ and electron/, .ts/.tsx, excluding common test patterns).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, "..", "..", "..");
const themeRoot = path.join(repoRoot, "vayeate-theme-studio");
const outDir = path.join(themeRoot, "code-review");
const outFile = path.join(outDir, "checklist.md");

const scanDirs = ["src", "electron"];
const extensions = new Set([".ts", ".tsx"]);

function isTestFile(relPath) {
  const n = relPath.replace(/\\/g, "/").toLowerCase();
  if (n.includes("/__tests__/")) return true;
  return /\.(test|spec)\.(tsx?|jsx?)$/.test(n);
}

function* walk(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const ent of entries) {
    const full = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      if (ent.name === "node_modules" || ent.name === "dist" || ent.name === "dist-electron") {
        continue;
      }
      yield* walk(full);
    } else {
      yield full;
    }
  }
}

const files = [];
for (const sub of scanDirs) {
  const base = path.join(themeRoot, sub);
  if (!fs.existsSync(base)) continue;
  for (const file of walk(base)) {
    const ext = path.extname(file);
    if (!extensions.has(ext)) continue;
    const rel = path.relative(themeRoot, file).replace(/\\/g, "/");
    if (isTestFile(rel)) continue;
    files.push(rel);
  }
}
files.sort();

const prefix = "vayeate-theme-studio/";
const lines = [
  "# Code review checklist",
  "",
  `Generated by \`generate-code-review-checklist.mjs\` (${files.length} files).`,
  "",
  ...files.map((f) => `- [ ] \`${prefix}${f}\``),
  "",
];

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, lines.join("\n"), "utf8");
console.log(`Wrote ${path.relative(themeRoot, outFile)} (${files.length} files).`);
